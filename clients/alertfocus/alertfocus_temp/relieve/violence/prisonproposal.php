<P><B>&nbsp;</B><B>Executive Summary</B></P>
<P>A mind gym is an exercise room for the brain to promote fitness for the nervous system.&nbsp; A healthy brain is free of problems that lead to criminality.&nbsp; This proposal is for a staffed and equipped exercise room filled with a dozen types of biofeedback and related devices. &nbsp;Two Trainers will instruct and guide Inmates in using the equipment in an physical exercise room model.&nbsp; </P>
<P>All these devices are intended to generally improve well-being (see references).&nbsp; &nbsp;Relaxing and exercising the brain and nervous system promotes increased functionality, resilience, and flexibility.&nbsp; No device is used to diagnose or treat medical or psychiatric conditions.&nbsp; Anyone will obtain various benefits from using the devices in their standard modes.&nbsp; The experience is pleasant.</P>
<P>Groups of Ten Inmates at a time will receive 30 sessions over 10 or 15 weeks lasting 90-minutes.&nbsp;&nbsp; This will be a relaxed and quiet room with most Trainees wearing headphones for their auditory feedback or looking at computers or hand-held devices. &nbsp;&nbsp;Any non-disruptive inmate is a candidate for the brain gym program.&nbsp; The strongest results are expected with the most violent and dysfunctional inmates, simply because they have more room for improvement.&nbsp; </P>
<P>Research has shown that biofeedback and related devices significantly relieve disorders such as anxiety, depression, insomnia, addictions, ADHD, learning disabilities, epilepsy, alcohol &amp; drug abuse, PTSD, and criminality (see references).&nbsp; Indeed, the 2776 felons at the Ontario Correctional facility between 1970 to 1995 had a 15% rate of recidivism after 3 years by using a combination of biofeedback and neurofeedback <SUP>7,8,9,10</SUP>.&nbsp; We hope to replicate and exceed these results with more advanced technology.&nbsp; Several assessments will be performed. </P>
