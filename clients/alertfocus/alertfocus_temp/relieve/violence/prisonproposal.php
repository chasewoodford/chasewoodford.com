<P><B>Biofeedback in a Mind Gym</B> </P>
<P>A mind gym is designed for maximum use of both staff Trainers and the diverse equipment.&nbsp; Trainers are not therapists.&nbsp; After trainee equipment orientation, most Trainer conversations are about dealing with the experience: “How do you feel?”&nbsp; “Breathe even slower.” “Do you want stop now?” “That’s great, keep on going.”&nbsp; “It’s time to move to this device now.”&nbsp;</P>
<P>There are dozens of devices in a mind gym.&nbsp; Most are peripheral biofeedback devices to train self-regulation of the autonomic nervous system.&nbsp; The other category is generic brain exercisers or relaxers that work on the central nervous system.&nbsp;&nbsp;</P>
<P>Some of these exercise brain waves others train to concentrate blood flow within the brain.&nbsp; Some equipment is for independent use and some brain biofeedback equipment is operated by a Trainer.&nbsp; One Trainer can operate several brain biofeedback devices at the same time.&nbsp;&nbsp;&nbsp;</P>
<H2>Training, not treatment</H2>
<P>The interventions proposed here are not treatment devices, but training equipment. The difference is that a treatment must include a diagnosis of a condition and prescribed protocols. Training is done without regard to medical or psychological condition.&nbsp; Any customization of training is related to the performance of the training itself, not to differential therapeutic interventions.&nbsp;&nbsp;&nbsp;</P>
<P>Again, these exercise devices train the brain and nervous system to foster self-awareness and self-control at conscious and unconscious levels.&nbsp; These interventions relieve disorders in clinical populations in that context.&nbsp; However, they also enhance performance of the best singers and soldiers.&nbsp; The analogy is to aerobic cross training which boosts any other athletic activity and which reduces hypertension.&nbsp;&nbsp;&nbsp;</P>
<H2>Adverse effects </H2>
<P>Adverse reactions are rare, minor and avoidable by proceeding gently.&nbsp;&nbsp;&nbsp;&nbsp;</P>
<P>Biofeedback is remarkably safe and benign. Unwanted reactions to training are rare, minor and self-limiting.&nbsp; Unwanted reactions may take the form of a brief headache or minor feelings of discomfort.&nbsp; These dissipate quickly after stopping.&nbsp; Many experienced practitioners have never seen a bad session &nbsp;including the co-author of the large prison study-practicing daily since 1959.&nbsp; As in any gym program, the basic principle is to proceed gently and check in with the Inmate as you go.&nbsp;&nbsp;&nbsp; Some devices must be purchased by a licensed clinician.&nbsp; The FDA permits sale of many biofeedback devices by the public.&nbsp;&nbsp;&nbsp;</P>
<H2>Introducing the devices to inmates </H2>
<P>Biofeedback is pleasant and interesting.&nbsp; The problem of introduction is a small issue.&nbsp; In my trial run on January 11, 2007 at the PIC maximum security prison I had no difficulty running eager 4 inmates at a time on 4 kinds of devices – even a pair of devices simultaneously.&nbsp; All the reports the dozen inmates gave me suggest that positive word of mouth will flourish.&nbsp;&nbsp;&nbsp;&nbsp;</P>
<P>The initial group of inmates should be a small and easy to manage group in order to ramp up operations while we all become familiar with the training procedures and security logistics. As the system is perfected we can accommodate those who are more reactive or who have a lower frustration tolerance.&nbsp;&nbsp;&nbsp;</P>
<H2>Features of Training </H2>
<P>After inmates achieve competence in using all the equipment, a set of personal goals will be recommended with a custom training circuit and plan for progression. Trainee preferences can be accommodated to some extent.&nbsp;</P>
<P>Biofeedback is essentially a non-verbal modality.&nbsp; It is all about self-interaction.&nbsp;&nbsp; Self-disclosure to another is not a part of the process.&nbsp; There are brief conversations for direction and about how you are feeling to resolve training and equipment issues.&nbsp;&nbsp;&nbsp;</P>
<P>No clinical records will be kept of sessions.&nbsp; Attendance and administrative records will be kept to schedule the mix of devices on each day.&nbsp; Nobody is going to be impressed with proof that inmates brain waves are less abnormal.&nbsp; An improvement in the inmates’ life is the prime criterion of success.&nbsp;&nbsp;&nbsp;</P>
<P>Almost anyone is a candidate for participation in the brain gym program.&nbsp; All that is required is that inmate not be too disruptive.&nbsp; Neurofeedback has been shown to reduce the need for psychoactive medications.&nbsp; Some of the devices are proven effective for ameliorating the pains of drug withdrawal.&nbsp;&nbsp;&nbsp;&nbsp;</P>
