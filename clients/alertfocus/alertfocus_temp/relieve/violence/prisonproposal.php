<H1>Purpose </H1>
<P>To set up, equip and staff a mental exercise room for inmates with mind-body training equipment including biofeedback devices. A small staff will serve as Trainers.&nbsp; The first year will entail the initial costs of equipment and training. A maintenance and replacement budget will enable the brain gym to be sustained in operation after the first year at a lower cost.&nbsp; </P>
