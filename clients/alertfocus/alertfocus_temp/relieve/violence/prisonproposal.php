<H2>Background</H2>
<P>Decades of research and clinical experience have demonstrated and proven that biofeedback and related technologies are an effective treatment for many disorders.&nbsp; See the references at the end for studies on anxiety, depression, insomnia, ADHD, learning disabilities, epilepsy, alcohol &amp; drug abuse, PTSD, and criminality.&nbsp; Overall, success rates range from 60-90% for the wide range of disorders commonly seen in prison populations.&nbsp;&nbsp;&nbsp;</P>
<P>From 1970 to 1995 a total of 2776 felons were treated at the Ontario Correctional Facility with peripheral biofeedback and brain wave biofeedback. No adverse effects were reported. This group averaged a recidivism rate after 3 years of just 15% <SUP>7,8,9,10</SUP>.&nbsp; &nbsp;Because these were often violent felons with a normal recidivism rate of 65% the program is estimated to have saved thousands of lives.&nbsp; </P>
<H2>Biofeedback <SUP>1,2,3</SUP></H2>
<P>Biofeedback is mind-body training.&nbsp; Biofeedback begins with monitoring an aspect of the body’s functioning.&nbsp; Biofeedback consists of interacting with a display of your physiology.&nbsp; This is often with a goal of relaxation.&nbsp;&nbsp;&nbsp;&nbsp;</P>
<P>When an inmate holds a digital thermometer with his fingers, he sees his temperature.&nbsp; As he relaxes, the temperature goes up.&nbsp; Using trial and error learning he trains his nervous system to relax in such a way as to get more blood flow to his hands.&nbsp; Practicing relaxing in this way enhances the capacity to relax at any time both consciously and unconsciously. </P>
<P>What’s happening here is a learning process and exercise of the brain and nervous system in better habits of psycho-physiological performance.&nbsp; The goal is to enhance self-regulation.&nbsp; Each device fosters greater mind-body communication and empowers greater range and flexibility of function.&nbsp; This is often about expanding the capacity to relax.&nbsp; The training unconsciously generalizes well to the rest of life including sleep.&nbsp; </P>
